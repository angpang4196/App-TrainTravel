package data.train;

public class TrainBody {
	TrainItems items;
	int numOfRows;
	int pageNo;
	int totalCount;

	public int getNumOfRows() {
		return numOfRows;
	}

	public int getPageNo() {
		return pageNo;
	}

	public int getTotalCount() {
		return totalCount;
	}

	public TrainItems getItems() {
		return items;
	}

}
